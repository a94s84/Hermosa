<template>
      <Loading :active="isLoading"></Loading>
     <div class="p-4">
      <div class="text-end mt-3">
        <button class="btn btn-warning btn-sm" type="button" @click="openModal(true)">增加產品</button>
      </div>
      <table class="table mt-4">
        <thead>
          <tr>
            <th width="10%">分類</th>
            <th width="35%">產品名稱</th>
            <th width="12%">原價</th>
            <th width="12%">售價</th>
            <th width="10%">是否啟用</th>
            <th width="19%" class="text-center">編輯</th>
          </tr>
        </thead>
        <tbody>
          <tr v-for="item in products" :key="item.id">
            <td>{{item.category}}</td>
            <td>{{item.title}}</td>
            <td class="text-right">{{ $filters.currency(item.origin_price) }}</td>
            <td class="text-right">{{ $filters.currency(item.price) }}</td>
            <td>
              <span class="text-success" v-if="item.is_enabled">啟用</span>
              <span class="text-muted" v-else>未啟用</span>
            </td>
            <td class="text-center">
              <div class="btn-group">
              <button class="btn btn-outline-primary btn-sm" @click="openModal(false, item)">編輯</button>
              <button class="btn btn-outline-danger btn-sm" @click="openDelModal(item)">刪除</button>
              </div>
            </td>
          </tr>
        </tbody>
      </table>
      <Pagination :pages="pagination" @emitPages="getProducts"></Pagination>
      <ProductModal ref="productModal" :modalProduct="tempProduct" @update-product="updateProduct"></ProductModal>
      <DelModal ref="delModal" @del-Product="delProduct" :delItem="tempProduct"></DelModal>
    </div>
</template>

<script>
import ProductModal from '@/components/ProductModal.vue'
import DelModal from '@/components/DelModal.vue'
import Pagination from '@/components/Pagination.vue'
// import { currency } from '../methods/filters'

export default {
  data () {
    return {
      products: [],
      pagination: {},
      tempProduct: {},
      isNew: false,
      isLoading: false
    }
  },
  components: {
    ProductModal, DelModal, Pagination
  },
  inject: ['emitter'],
  methods: {
    // currency,
    openModal (isNew, item) {
      if (isNew) {
        this.tempProduct = {}
      } else {
        this.tempProduct = { ...item }
      }
      this.isNew = isNew
      this.$refs.productModal.showModal()
    },
    getProducts (page = 1) {
      const api = `${process.env.VUE_APP_API}api/${process.env.VUE_APP_PATH}/admin/products/?page=${page}`
      this.isLoading = true
      this.$http.get(api).then((res) => {
        this.isLoading = false
        if (res.data.success) {
          // console.log(res.data)
          this.products = res.data.products
          this.pagination = res.data.pagination
        }
      })
    },
    updateProduct (item) {
      this.tempProduct = item
      // 更新
      let api = `${process.env.VUE_APP_API}api/${process.env.VUE_APP_PATH}/admin/product`
      let httpMethod = 'post'
      // 編輯
      if (!this.isNew) {
        api = `${process.env.VUE_APP_API}api/${process.env.VUE_APP_PATH}/admin/product/${item.id}`
        httpMethod = 'put'
      }
      const productComponet = this.$refs.productModal
      this.$http[httpMethod](api, { data: this.tempProduct }).then((res) => {
        productComponet.hideModal()
        this.$httpMessageState(res, '更新成功')
        // if (res.data.success) {
        //   this.getProducts()
        //   this.emitter.emit('push-message', {
        //     style: 'success',
        //     title: '更新成功'
        //   })
        // } else {
        //   this.emitter.emit('push-message', {
        //     style: 'danger',
        //     title: '更新失敗',
        //     content: res.data.message.join('、')
        //   })
        // }
      })
    },
    openDelModal (item) {
      this.tempProduct = { ...item }
      this.$refs.delModal.showModal()
    },
    delProduct () {
      const api = `${process.env.VUE_APP_API}api/${process.env.VUE_APP_PATH}/admin/product/${this.tempProduct.id}`
      this.$http.delete(api).then((res) => {
        const delComponet = this.$refs.delModal
        delComponet.hideModal()
        this.emitter.emit('push-message', {
          style: 'success',
          title: '成功刪除'
        })
        this.getProducts()
      })
    }
  },
  created () {
    this.getProducts()
  }
}
</script>
